const mongoose = require('mongoose');

//creates new product schema
const ProductSchema = new mongoose.Schema({
    title: {
        type: String,
        required: true
    },
    description: {
        type: String,
        required: true
    },
    url: {
        type: String,
        required: true
    },
    createdAt: {
        type: Date,
        default: Date.now
    }
});

//registering model so the application can know it exists
mongoose.model('Product', ProductSchema);